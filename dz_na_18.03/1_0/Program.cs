/* Задайте массив заполненный случайными положительными трехзначными
числами . Напишите программу, которая покажет количество четных чисел в массиве . */

    int[] arr = new int[100]; // создаем массив из 100 элементов
        int evenCount = 0; // счетчик четных чисел

        // заполняем массив случайными трехзначными числами
        Random rnd = new Random();
        for (int i = 0; i < arr.Length; i++) {
            arr[i] = rnd.Next(100, 1000);
        }

        // ищем четные числа и увеличиваем счетчик
        for (int i = 0; i < arr.Length; i++) {
            if (arr[i] % 2 == 0) {
                evenCount++;
            }
        }

        // выводим результат
        Console.WriteLine("Количество четных чисел: " + evenCount);