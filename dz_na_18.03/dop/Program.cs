/* Найдите произведение пар чисел в одномерном массиве. Парой считаем первый и последний элемент, 
второй и последний и т.д. Результат запишите в новом массиве . */

int[] arr = new int[5] { 1, 2, 3, 4, 5 }; // создаем массив из 5 целых чисел
int[] result = new int[arr.Length / 2]; // создаем массив для результатов

// вычисляем произведения пар чисел и записываем результаты в новый массив
for (int i = 0; i < arr.Length / 2; i++)
{
    result[i] = arr[i] * arr[arr.Length - i - 1];
}

// выводим результат
Console.WriteLine("Произведения пар чисел в массиве:");
for (int i = 0; i < result.Length; i++)
{
    Console.WriteLine(result[i]);
}