// Напишите программу, которая будет создавать копию 
// заданного массива с помощью поэлементного копирования.
int[] arr1 = { 1, 2, 3, 4, 5 }; // заданный массив
int[] arr2 = new int[arr1.Length]; // создаем пустой массив той же длины

// поэлементно копируем значения из arr1 в arr2
for (int i = 0; i < arr1.Length; i++)
{
    arr2[i] = arr1[i];
}

// выводим оба массива для проверки
Console.WriteLine("arr1: [" + string.Join(", ", arr1) + "]");
Console.WriteLine("arr2: [" + string.Join(", ", arr2) + "]");