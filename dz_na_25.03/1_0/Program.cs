//Задайте двумерный массив размером m×n, заполненный случайными вещественными числами.
Console.Write("Введите количество строк: ");
int m = int.Parse(Console.ReadLine()); // считываем количество строк
Console.Write("Введите количество столбцов: ");
int n = int.Parse(Console.ReadLine()); // считываем количество столбцов

double[,] arr = new double[m, n]; // создаем двумерный массив

Random rand = new Random(); // создаем объект для генерации случайных чисел

// заполняем массив случайными вещественными числами
for (int i = 0; i < m; i++)
{
    for (int j = 0; j < n; j++)
    {
        arr[i, j] = rand.NextDouble() * 10; // генерируем случайное число от 0 до 10
    }
}

// выводим массив на экран для проверки
Console.WriteLine("Массив:");
for (int i = 0; i < m; i++)
{
    for (int j = 0; j < n; j++)
    {
        Console.Write($"{arr[i, j]:F2}\t"); // выводим каждый элемент массива с двумя знаками после запятой
    }
    Console.WriteLine(); // переводим строку после каждой строки массива
}
